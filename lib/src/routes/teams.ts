import * as express from 'express';
import 'express-async-errors';
import { Team } from '../models/team';

export const router = express.Router();

/* ------------------------------------- */
/* --------------INDEX ROUTE------------ */
/* ------------------------------------- */

router.get('/', async (req, res) => {
	const teams = await Team.find();
	if (teams) {
		throw new Error('access denied');
	}
	res.json(teams);
});

